package com.example.studentauth.entity;

public class ReachDesc {
    private Integer wordId;
    private String reachRecord;
    private String reachChange;
    private String reachResources;
    private Integer excelId;

    public Integer getWordId() {
        return wordId;
    }

    public void setWordId(Integer wordId) {
        this.wordId = wordId;
    }

    public String getReachRecord() {
        return reachRecord;
    }

    public void setReachRecord(String reachRecord) {
        this.reachRecord = reachRecord;
    }

    public String getReachChange() {
        return reachChange;
    }

    public void setReachChange(String reachChange) {
        this.reachChange = reachChange;
    }

    public String getReachResources() {
        return reachResources;
    }

    public void setReachResources(String reachResources) {
        this.reachResources = reachResources;
    }

    public Integer getExcelId() {
        return excelId;
    }

    public void setExcelId(Integer excelId) {
        this.excelId = excelId;
    }


}
