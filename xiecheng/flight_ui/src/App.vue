<template>
  <a-layout id="main-layout" class="layout">
    <a-layout-header>
      <div class="logo">航班订票系统</div>
      <a-menu
        theme="dark"
        mode="horizontal"
        :default-selected-keys="current"
        :style="{ lineHeight: '64px' }"
      >
      <a-menu-item key="/">
        <router-link to="/">首页</router-link>
      </a-menu-item>
        <!-- 普通用户路由 -->
        <a-menu-item key="/flight-list" v-if="user_info.role == 'user'">
          <router-link to="/flight-list">航班列表</router-link>
        </a-menu-item>
        <a-menu-item key="/order" v-if="user_info.role == 'user'">
          <router-link to="/order">我的订单</router-link>
        </a-menu-item>
        <a-menu-item key="/message" v-if="user_info.role == 'user'">
            <router-link to="/message">留言</router-link>
        </a-menu-item>
        <!-- 管理员路由 -->
        <a-menu-item key="/flight-manage" v-if="user_info.role == 'admin'">
          <router-link to="/flight-manage">航班管理</router-link>
        </a-menu-item>
        <a-menu-item key="/user-manage" v-if="user_info.role == 'admin'">
          <router-link to="/user-manage">用户管理</router-link>
        </a-menu-item>
        <a-menu-item key="/message-manage" v-if="user_info.role == 'admin'">
          <router-link to="/message-manage">留言管理</router-link>
        </a-menu-item>
        <!-- 公共信息 -->
        <a-sub-menu v-if="showLR" title="个人中心">
          <a-menu-item>
            <router-link to="/login">登录</router-link>
          </a-menu-item>
          <a-menu-item>
            <router-link to="/register">注册</router-link>
          </a-menu-item>
        </a-sub-menu>
        <a-sub-menu v-if="!showLR" :title="'用户: ' + user_info.nickname">
          <a-menu-item>
            <router-link to="/profile">个人信息</router-link>
          </a-menu-item>
          <a-menu-item>
            <a @click="logout">登出</a>
          </a-menu-item>
        </a-sub-menu>
      </a-menu>
    </a-layout-header>
    <a-layout-content style="padding: 30px 50px 0px">
      <div :style="{ background: '#fff', padding: '24px', minHeight: '640px' }">

        <!-- 页面缓存，进入这个页面不再刷新 -->
        <keep-alive>
          <router-view v-if="$route.meta.keepAlive" :user_info="user_info"></router-view>
        </keep-alive>
        <router-view  v-if="!$route.meta.keepAlive" :user_info="user_info" />
      </div>
    </a-layout-content>
    <a-layout-footer style="text-align: center">
      <Footer />
    </a-layout-footer>
  </a-layout>
</template>

<script>
import Footer from "@/components/Footer.vue";

export default {
  data() {
    return {
      current: [],
      user_info: {},
      showLR: true,
    };
  },
  components: {
    Footer,
  },
  beforeUpdate() {
    // 加载菜单
    this.current[0] = this.$route.path;
  },
  updated() {},
  mounted() {
    this.loadUserInfo();
  },
  watch: {},
  methods: {
    /**
     * 加载用户信息，校验用户是否登录超时
     */
    loadUserInfo() {
      if (!localStorage.token) {
        return;
      }
      this.$user_api.user_info().then((res) => {
        // 请求后端数据
        if (res.code == 200) {
          this.$message.success("用户信息获取成功");
          this.user_info = res.data;
          this.showLR = false;
          if (this.$route.path == '/login') {
            this.$router.push({
              path: '/',
            });
          } 
          console.log(this.user_info);
        } else {
          this.$message.error("用户信息获取失败:" + res.msg);
          localStorage.removeItem("token");
          this.$router.push({
            path: `/login`,
          });
        }
      });
    },
    // 登出操作
    logout() {
      this.$user_api.logout().then((res) => {
        if (res.code == 200) {
          localStorage.clear();
          location.reload();
        } else {
          this.$message.error("登出失败！！");
        }
      });
    },
  },
};
</script>

